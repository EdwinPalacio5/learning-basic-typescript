var nombre1:string = 'Juan'
var nombre2:string = 'juan'

var verdadero:boolean = true
var falso:boolean = false

//Igualdad
var sonNombresIguales:boolean = nombre1 == nombre2 
console.log("Los nombres son iguales: ",sonNombresIguales)

//comparador &&
var sonAmbosVerdaderos= verdadero && falso
console.log("ambos son verdaderos:", sonAmbosVerdaderos)

//Comparador ||
var algunoEsVerdadero:boolean = verdadero || falso
console.log("Alguno es verdadero:", algunoEsVerdadero)


